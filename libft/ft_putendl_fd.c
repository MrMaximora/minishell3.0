#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	pos;

	pos = 0;
	if (!s)
		return ;
	while (s[pos])
	{
		write(fd, &s[pos], 1);
		pos++;
	}
	write (fd, "\n", 1);
}
